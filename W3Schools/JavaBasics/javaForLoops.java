public class javaForLoops {
    public static void main(String[] args) {
        // basic example
        for(int i=0; i<5; i++) {
            System.out.println(i);
        }

        // for-each loop
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        for(String i : cars) {
            System.out.println(i);
        }

    }
}
